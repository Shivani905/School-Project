package datatypestudy;

public class NumberwithDecimal {

	public static void main(String[] args)
	{
		float num;
		num = 1236547.23654165845669246f;
		System.out.println("Value is "+num);
		 
		double num1;
		num1=123655.468464564d;
		System.out.println("Value of num1 is "+num1);
		
		long mob;
		mob= 98745632101l;
		System.out.println("Mobile number is "+mob);
		
		float percentage;
		percentage=98.12f;
		char symbol='%';
		System.out.println("My percentage is "+percentage+symbol);
		
		float height = 5.5f;
		System.out.println("My height is "+height);
		 
		char gender;
		gender='F';
		System.out.println("My gender is "+gender);
		
		boolean signal;
		signal =true;
		System.out.println("Signal ststus is "+signal);
		

	}

}
